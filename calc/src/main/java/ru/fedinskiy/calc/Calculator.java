public class Calculator {
	public int minus(int arg1, int arg2) {
		return arg1 - arg2;
	}
	
	public int summ(int arg1, int arg2) {
		return arg1 + arg2;
	}
	
	public int multiply(int arg1, int arg2) {
		return arg1 * arg2;
	}
	
	public int divide(int arg1, int arg2) {
		return arg1 / arg2;
	}
}
