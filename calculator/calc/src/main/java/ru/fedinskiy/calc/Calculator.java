public class Calculator {
	public static int minus(int arg1, int arg2){
		return arg1-arg2;
	}
	
	public static int summ(int arg1, int arg2){
		return arg1+arg2;
	}
	public static int multiply(int arg1, int arg2){
		return arg1*arg2;
	}
	public static int divide(int arg1, int arg2){
		return arg1/arg2;
	}
}